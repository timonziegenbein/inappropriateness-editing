def _group_edits_by_sentence(edits: List[Dict[str, Any]]) -> Dict[str, List[Dict[str, str]]]:
    """
    Group edits by their original sentence.

    Args:
        edits: List of edits with 'original_sentence', 'inappropriate_part', 'rewritten_part'

    Returns:
        Dict mapping sentence text to list of edits in that sentence
    """
    edits_by_sentence = {}

    for edit in edits:
        original_sentence = edit.get('original_sentence', '')
        if not original_sentence:
            continue

        if original_sentence not in edits_by_sentence:
            edits_by_sentence[original_sentence] = []

        edits_by_sentence[original_sentence].append({
            'inappropriate_part': edit.get('inappropriate_part'),
            'rewritten_part': edit.get('rewritten_part')
        })

    return edits_by_sentence


